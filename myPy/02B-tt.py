import turtle as t

#삼각형 그리기
t.fd(100)   #거북이가 100만큼 앞으로 이동합니다.
t.lt(120)   #거북이가 왼쪽으로 120도 회전합니다.
t.fd(100)   #위 과정을 두 번 반복합니다.
t.lt(120)
t.fd(100)
t.lt(120)

#사각형 그리기
t.fd(100)   #거북이가 100만큼 앞으로 이동합니다.
t.lt(90)    #거북이가 왼쪽으로 90도 회전합니다.
t.fd(100)   #위 과정을 세 번 반복합니다.
t.lt(90)
t.fd(100)
t.lt(90)
t.fd(100)
t.lt(90)

#원 그리기
t.circle(50)    #반지름이 50인 원을 그립니다.
